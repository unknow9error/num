use crate::span::Span;

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum TokenKind {
    Ident(String),
    Number(String),
    String(String),
    Keyword(Keyword),
    Symbol(Symbol),
    Newline,
    Eof,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum Keyword {
    Action,
    Actor,
    Allow,
    Assert,
    As,
    Budget,
    Connector,
    Cost,
    Deny,
    Else,
    Enum,
    Event,
    Fn,
    For,
    From,
    If,
    Impl,
    Idempotency,
    Input,
    Internal,
    Let,
    Match,
    Module,
    Permission,
    Policy,
    Private,
    Public,
    Rate,
    Regulated,
    Requires,
    Require,
    Return,
    Retry,
    Risk,
    Role,
    Rollback,
    Saga,
    Secret,
    Sensitive,
    Service,
    Test,
    Timeout,
    Transaction,
    Trusted,
    Type,
    Untrusted,
    Use,
    Var,
    Verified,
    Workflow,
    Async,
    Await,
    Scope,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum Symbol {
    Arrow,
    FatArrow,
    Colon,
    Comma,
    Dot,
    Eq,
    EqEq,
    BangEq,
    LBrace,
    RBrace,
    LParen,
    RParen,
    Lt,
    LtEq,
    Gt,
    GtEq,
    Pipe,
    PipePipe,
    AmpAmp,
    Plus,
    Minus,
    Star,
    Slash,
    Question,
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Token {
    pub kind: TokenKind,
    pub lexeme: String,
    pub span: Span,
}

impl Token {
    pub fn eof(source: impl Into<String>, offset: usize, line: usize, column: usize) -> Self {
        Self {
            kind: TokenKind::Eof,
            lexeme: String::new(),
            span: Span::new(source, offset, offset, line, column),
        }
    }
}

